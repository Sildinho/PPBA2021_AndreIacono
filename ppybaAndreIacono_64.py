# 64. Conhecendo a Função Lambda

# lambda function
    # é um função (pequena) sem nome
    # pode ter varios argumentos mas coment 1 expressao
    # muito utilizada dentro de outras funções
    # codigos mais "clean"


def somar10(x):
    return x+10
print(somar10(2))


def somar100(x, y):
    return x+y+10
print(somar100(2, 4))
