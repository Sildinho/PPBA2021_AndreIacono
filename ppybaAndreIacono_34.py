# 34. Conhecendo o While Loop

# loop que depende de um condição

# criar um promoção para um produto no vlaor de R$ 100.00


valor = 100.00
dia = 1
# valor nao pode ser menor que 20.
# enquanto valor for maior que 20.
while valor > 20.00:
    print(f'No dia {dia}, o produto será vendido por R$ {valor}')
    dia+= 1
    # print(valor)
    valor -=10





